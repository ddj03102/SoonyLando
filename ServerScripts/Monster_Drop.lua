function MyReward(a, b, HP)
	if a==nil or b==nil or HP==nil then
		return
	end
	
	local W = b.monsterID
	
	for i, v in ipairs(mon_list[W]['dataID']) do
		if rand(1,100001) <= mon_list[W]['chanse'][i]*1000 then
			if mon_list[W]['bool'][i] then
				a.AddItem(mon_list[W]['dataID'][i], mon_list[W]['count'][i])
				a.FireEvent('Twinkle', mon_list[W]['dataID'][i], 4, 8)
				Server.SendSay(a.player.name.."님께서 "..Server.GetMonster(W).name.."을(를) 격파하고 "..Server.GetItem(mon_list[W]['dataID'][i]).name.."를 획득하셨습니다.")
			else
				a.AddItem(mon_list[W]['dataID'][i], mon_list[W]['count'][i])
			end
			
		end
	end
	a.FireEvent('BAR_EXP')
end
Server.rewardCallback = MyReward